// Rational Operators

let ap=45;
let hd=78;

// ==
console.log(ap==hd);
console.log(376==376);
console.log(376=="376");
console.log("superman"=="superman");
console.log("superMan"=="superman");
console.log("Hello"+"**************************************"+"World");   //concatination
// ===
console.log(ap===hd); 
console.log(376===376);
console.log(376==="376");
console.log("superman"==="superman");
console.log("superMan"==="superman");

console.log("Hello"+"\n"+"**************************************"+"\n"+"World");   //concatination

// !=
console.log(ap!=hd); 
console.log(376!=376);
console.log(376!="376");
console.log("superman"!="superman");
console.log("superMan"!="superman");
console.log("Hello"+"**************************************"+"World");   //concatination

// !==
console.log(ap!==hd); 
console.log(376!==376);
console.log(376!=="376");
console.log("superman"!=="superman");
console.log("superMan"!=="superman");